package com.ae.ae1.String.ex1;

public class WeatherDTO {
	
	private String city;
	private String gion;
	private String hum; //습도
	private String codition;
	
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getGion() {
		return gion;
	}
	public void setGion(String gion) {
		this.gion = gion;
	}
	public String getHum() {
		return hum;
	}
	public void setHum(String hum) {
		this.hum = hum;
	}
	public String getCodition() {
		return codition;
	}
	public void setCodition(String codition) {
		this.codition = codition;
	}
	

}
